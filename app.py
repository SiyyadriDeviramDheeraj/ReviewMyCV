def generate_pdf_report(score, matched, missing, fit_level):
    pdf = FPDF()
    pdf.add_page()


    pdf.set_font("Arial", 'B', 16)
    pdf.cell(200, 10, "ReviewMyCV: Analysis Report", ln=True, align='C')
    pdf.ln(10)


    pdf.set_font("Arial", 'B', 12)
    pdf.cell(200, 10, f"Match Score: {score}%", ln=True)
    pdf.set_font("Arial", '', 11)
    pdf.cell(200, 10, f"Fit Level: {fit_level}", ln=True)
    pdf.ln(5)


    pdf.set_font("Arial", 'B', 12)
    pdf.cell(200, 10, "Matched Skills:", ln=True)
    pdf.set_font("Arial", '', 10)
    if matched:
        for s in matched:
            pdf.cell(200, 8, f"- {s}", ln=True)
    else:
        pdf.cell(200, 8, "- None detected", ln=True)
    pdf.ln(5)

    # Missing Skills
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(200, 10, "Missing Skills (Gaps):", ln=True)
    pdf.set_font("Arial", '', 10)
    if missing:
        for s in missing:
            pdf.cell(200, 8, f"x {s}", ln=True)
    else:
        pdf.cell(200, 8, "- No gaps found!", ln=True)

    # Footer
    pdf.ln(20)
    pdf.set_font("Arial", 'I', 8)
    pdf.cell(200, 10, "Report generated by ReviewMyCV Optimizer", align='C')

    return pdf.output(dest='S').encode('latin-1')


